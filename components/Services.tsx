export default function Services() {
  const [services, setServices] = useState<Service[]>([])
  const [loading, setLoading] = useState(true)
  const [error, setError] = useState<string | null>(null)

  const loadServices = async () => {
    setLoading(true)
    setError(null)

    const response = await fetchServices()

    if (response.success && response.data) {
      setServices(response.data.services)
    } else {
      setError(response.error || 'Failed to load services')
    }

    setLoading(false)
  }

  useEffect(() => {
    loadServices()
  }, [])

  if (loading) {
    return (
      <section id="services" className="section-padding bg-gradient-to-b from-secondary-50 to-white">
        <Loading message="Loading services..." />
      </section>
    )
  }

  if (error) {
    return (
      <section id="services" className="section-padding bg-gradient-to-b from-secondary-50 to-white">
        <ErrorMessage message={error} onRetry={loadServices} />
      </section>
    )
  }

  return (
    <section id="services" className="section-padding bg-gradient-to-b from-secondary-50 to-white relative overflow-hidden">
      {/* Decorative background */}
      <div className="absolute inset-0 tech-grid opacity-20" />

      <div className="container-custom relative z-10">
        <motion.div
          initial={{ opacity: 0, y: 20 }}
          whileInView={{ opacity: 1, y: 0 }}
          viewport={{ once: true }}
          transition={{ duration: 0.6 }}
          className="text-center max-w-3xl mx-auto mb-16"
        >
          <span className="px-4 py-2 bg-accent/10 text-accent rounded-full text-sm font-semibold mb-4 inline-block border border-accent/20">
            Our Services
          </span>
          <h2 className="heading-lg mb-4">
            Comprehensive IT Integration Solutions
          </h2>
          <p className="text-lg text-secondary-600">
            From structured cabling to cloud infrastructure, we deliver enterprise-grade IT integration solutions that power modern businesses.
          </p>
        </motion.div>

        <div className="grid md:grid-cols-2 lg:grid-cols-4 gap-6">
          {services.map((service, index) => {
            const icon = iconMap[service.slug] || iconMap['business-solutions']

            return (
              <motion.div
                key={service.id}
                initial={{ opacity: 0, y: 20 }}
                whileInView={{ opacity: 1, y: 0 }}
                viewport={{ once: true }}
                transition={{ delay: index * 0.1, duration: 0.5 }}
                whileHover={{ y: -8, boxShadow: '0 10px 40px rgba(30, 64, 175, 0.3)' }}
                className="tech-card group"
              >
                <div className="w-14 h-14 bg-gradient-to-br from-accent/10 to-primary-100 rounded-xl flex items-center justify-center text-accent mb-4 group-hover:shadow-glow transition-all duration-300">
                  {icon}
                </div>
                <h3 className="text-xl font-bold mb-4 group-hover:text-accent transition-colors">{service.title}</h3>
                <ul className="space-y-2 mb-6">
                  {service.subServices.map((subService, idx) => (
                    <li key={idx} className="text-secondary-600 text-sm flex items-start gap-2">
                      <svg className="w-4 h-4 text-accent flex-shrink-0 mt-0.5" fill="none" viewBox="0 0 24 24" stroke="currentColor">
                        <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M9 12l2 2 4-4m6 2a9 9 0 11-18 0 9 9 0 0118 0z" />
                      </svg>
                      <span>{subService}</span>
                    </li>
                  ))}
                </ul>
                <a href={service.link} className="text-accent font-semibold flex items-center gap-2 group mt-auto">
                  Learn More
                  <svg
                    className="w-5 h-5 group-hover:translate-x-1 transition-transform"
                    fill="none"
                    viewBox="0 0 24 24"
                    stroke="currentColor"
                  >
                    <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M17 8l4 4m0 0l-4 4m4-4H3" />
                  </svg>
                </a>
              </motion.div>
            )
          })}
        </div>
      </div>
    </section>
  )
}
